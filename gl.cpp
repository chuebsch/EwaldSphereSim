void GL::mouseMoveEvent(QMouseEvent *event){
  bool g=(event->modifiers()&Qt::ShiftModifier);
   GLfloat dx = GLfloat(
      event->x() - lastPos.x()) / width();
   GLfloat dy = GLfloat(
      event->y() - lastPos.y()) / height();
   if (event->buttons() & Qt::MidButton){
     glTranslateL(dx*1,-dy*1,0);
     updateGL();
   }
   if ((event->buttons() & Qt::LeftButton)) {
     if (g){
       double s = sin(dy*M_PI);
       double c = cos(dy*M_PI);
       
       __RotateCS( c, s, rm.m12, rm.m13 );
       __RotateCS( c, s, rm.m22, rm.m23 );
       __RotateCS( c, s, rm.m32, rm.m33 );
       s = sin(dx*M_PI);
       c = cos(dx*M_PI);
       __RotateCS( c, s, rm.m13, rm.m11 );
       __RotateCS( c, s, rm.m23, rm.m21 );
       __RotateCS( c, s, rm.m33, rm.m31 );
       cryrot();

     }else{
      glRotateL(dy*360.0,1.0f,0.0f,0.0f);
      glRotateL(dx*360.0,0.0f,1.0f,0.0f);
      updateGL();
     }
   }else if((event->buttons() & Qt::RightButton)){
     glScaled(1.0-dy,1.0-dy,1.0-dy);
      updateGL();
   }

   lastPos = event->pos();
}

void GL::wheelEvent(QWheelEvent *event){
   double d=event->delta()/120000.0;
   rbal+=d;
   rbal=(rbal<0.0001)?0.0001:rbal;
   rbal=(rbal>0.2)?0.2:rbal;
   updateGL();
}

