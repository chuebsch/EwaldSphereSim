inline void __RotateCS( double c, double s, double& X, double& Y ) {
  double T = X;
  X = c*X - s*Y;
  Y = s*T + c*Y;
}

void glTranslateL( const double dx, const double dy, const double dz ) {
  double mat[4][4];

  glGetDoublev( GL_MODELVIEW_MATRIX, (double*)mat );
  mat[3][0] += dx;  mat[3][1] += dy;  mat[3][2] += dz;
  glLoadMatrixd((double*)mat);
}

void GL::cryrot(){
  A=V3(a,0,0);
  B=V3(b*cosga,b*singa,0);
  C=V3(c*cosbe,tau,c * cphi / singa);
  RA=(B%C)*rV;
  RB=(C%A)*rV;
  RC=(A%B)*rV;
  double cp=cos(phi/g2r);
  double sp=sin(phi/g2r);
  Matrix p=Matrix(cp,0,sp,0,1,0,-sp,0,cp);
  double cc=cos(chi/g2r);
  double sc=sin(chi/g2r);
  Matrix c=Matrix(1,0,0,0,cc,sc,0,-sc,cc);
  double co=cos(omega/g2r);
  double so=sin(omega/g2r);
  Matrix o=Matrix(co,0,so,0,1,0,-so,0,co);
  orient=(p*c)*o;
  orient=orient*rm;
  A=A*orient;
  B=B*orient;
  C=C*orient;
  RA=RA*orient;
  RB=RB*orient;
  RC=RC*orient;
  updateGL();
}

