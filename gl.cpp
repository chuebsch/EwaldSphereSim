int GL::absent(int h,int k, int l){
  V3 hkl=V3(h,k,l);
  for (int n=0; n<symmops.size(); n++){
    V3 shkl=symmops.at(n)*hkl;
    if ((hkl-shkl)==V3(0,0,0)){
      if (fabs(fmod(0.5+fabs(hkl*trans.at(n)),1.0)-0.5)>0.1){
//        printf("%4d%4d%4d is systematically absent!\n",(int)hkl.x,(int)hkl.y,(int)hkl.z);
        return 1;
      }
    }
  }
  return 0;
}

double GL::winkel(V3 a,V3 b){

  static double erg;
  erg=(a.x*b.x+a.y*b.y+a.z*b.z)/(sqrt(a.x*a.x+a.y*a.y+a.z*a.z)*sqrt(b.x*b.x+b.y*b.y+b.z*b.z));
  erg=acos(erg)/M_PI*180.0;
 return(erg);
}

